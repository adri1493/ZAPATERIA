    private void btnGuardarNueMarcaActionPerformed(java.awt.event.ActionEvent evt) { //GEN-FIRST:event_btnGuardarNueMarcaActionPerformed
        Boolean igual = false;
        if (!(txtNombreMarcas.getText().equals(""))) {
            for (Marca unaMarca : unaEmpresa.getMarcas().values())
                if (txtNombreMarcas.getText().equals(unaMarca.getNombreMarca()))
                    igual = true;
            if (!lstMarca.isEnabled()) {
                try {
                    unaEmpresa.modificarMarca(lstMarca.getSelectedValue().toString(), txtNombreMarcas.getText());
                    JOptionPane.showMessageDialog(null, "La marca se modifico con exito");
                    txtNombreMarcas.setText("");
                    txtNombreMarcas.requestFocus();
                } catch (Exception e) {
                }
                lstMarca.setEnabled(true);
            } else {
                try {
                    if (aprobado()) {
                        unaEmpresa.agregarMarca(txtNombreMarcas.getText());
                        JOptionPane.showMessageDialog(null, "La marca se cargo con exito");
                        txtNombreMarcas.setText("");
                        txtNombreMarcas.requestFocus();
                    }
                    //cargar(unaEmpresa.getMarcas());
                    else
                        JOptionPane.showMessageDialog(null, "el nombre del articulo ya existe");
                } catch (Exception e) {
                    //JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }
            //cargar(unaEmpresa.getMarcas());
        } else
            JOptionPane.showMessageDialog(null, "No ingreso el Nombre", null, JOptionPane.INFORMATION_MESSAGE);


    } //GEN-LAST:event_btnGuardarNueMarcaActionPerformed

