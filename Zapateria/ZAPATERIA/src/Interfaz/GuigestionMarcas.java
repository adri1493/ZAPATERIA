    public void cargar(Map marcas) {
        Collection lista;
        lista = marcas.values();
        DefaultListModel modelo = new DefaultListModel();
        if (!lista.isEmpty()) {
            int i = 0;
            for (Object unObj : lista) {
                Marca unaMarca = (Marca) unObj;
                modelo.addElement(unaMarca.getNombreMarca());
            }
            lstMarca.setModel(modelo);
        }
    }

    @Override
    public void update(Observable observable, Object object) {
        cargar(marcas);

    }

    private void reloj() {
        javax.swing.Timer timer = new javax.swing.Timer(1000, new java.awt.event.ActionListener() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent ae) {
                lblFecha.setText(Calendar.getInstance().getTime().toLocaleString());
            }
        });
        timer.start();
    }
    
    private Boolean aprobado() {
        Boolean existe = true;
        if (!(txtNombreMarcas.getText().equals(""))) {
            for (TipoPago tipo : unaEmpresa.getTiposPagos().values()) {
                if (tipo.getNombre().equals(txtNombreMarcas.getText()))
                    existe = false;
            }
        } else
            existe = false;
        return existe;
    }
