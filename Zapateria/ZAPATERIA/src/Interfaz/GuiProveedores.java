    public Map consultaProveedor(String consulta) {
        int i = cmbCriterioProveedor.getSelectedIndex();
        Map retorno = new HashMap();
        Proveedor aux = null;
        if(consulta.equals(""))
            return unaEmpresa.getProveedores();
        for (Object unProveedor : unaEmpresa.getProveedores().values().toArray()) {
            aux = (Proveedor) unProveedor;
            if ((i == 0) &&
                (aux.getCUITPro().toUpperCase().contains(consulta) || aux.getRazonSocial().toUpperCase().contains(consulta) ||
                 aux.getDomPro().toUpperCase().contains(consulta) || aux.getDomPro().toUpperCase().contains(consulta))) {
                retorno.put(aux.getCUITPro(), aux);
            }
            if ((i == 1) && (aux.getCUITPro().toUpperCase().contains(consulta))) {
                retorno.put(aux.getCUITPro(), aux);
            }
            if ((i == 2) && (aux.getRazonSocial().toUpperCase().contains(consulta))) {
                retorno.put(aux.getRazonSocial(), aux);
            }
            if ((i == 3) && (aux.getDomPro().toUpperCase().contains(consulta))) {
                retorno.put(aux.getDomPro(), aux);
            }
        }
        return retorno;
    }

    public static Boolean ValidaCuit(String cuit) {
        Boolean valido = false;
        int factor = 5;
        int[] c = new int[11];
        c[10] = Integer.parseInt(cuit.substring(10));
        int resultado = 0;

        for (int i = 0; i < 10; i++) {
            // se toma el valor de cada cifra
            c[i] = Integer.parseInt(Character.toString(cuit.charAt(i)));
            // se suma al resultado el producto de la cifra por el factor que
            // corresponde resultado = resultado + c[i]* factor;
            resultado = resultado + c[i] * factor;
            // se actualiza el valor del factor
            factor = (factor == 2) ? 7 : factor - 1;
        }
        // se obtiene el valor calculado a comparar
        int control = (11 - (resultado % 11)) % 11;
        // Si la cifra de control es igual al valor calculado
        if (control == c[10]) {
            valido = true;
        } else {
            valido = false;
        }
        return valido;
    }
    
