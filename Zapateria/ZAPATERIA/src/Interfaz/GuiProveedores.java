    public void CargaTabla(Map proveedores) {
        DefaultTableModel modelo = (DefaultTableModel) tblProveedores.getModel();
        Collection lista;
        int filas = tblProveedores.getRowCount();
        for (int i = 0; filas > i; i++)
            modelo.removeRow(0);
        lista = proveedores.values();
        if (!lista.isEmpty()) {
            int i = 0;
            for (Object unObj : lista) {
                Proveedor unPro = (Proveedor) unObj;
                modelo.addRow(new Object[] { (unPro.getRazonSocial()), (unPro.getCUITPro()) });
            }
        }
    }

