    public void guardar() {
        try {

            Calendar cal1 = Calendar.getInstance();
            if (Double.parseDouble(txtSaldo.getText()) <= 0) {
                JOptionPane.showMessageDialog(null, "El Saldo de Apertura es menor o igual cero.");
                txtSaldo.requestFocus();
            } else {
                codCaja = unaEmpresa.agregarCaja(cal1, Double.parseDouble(txtSaldo.getText()), true, GuiIngresar.getUsuarioActual());
                if (cal1.get(Calendar.MONTH) != unaEmpresa.getMes()) {
                    unaEmpresa.setMes(cal1.get(Calendar.MONTH));
                    for (Articulo unArt : unaEmpresa.getArticulos().values()) {
                        unArt.setVentasMes(0);
                        unArt.setEnVidriera(false);
                    }
                    for (Venta unaVen : unaEmpresa.getVentas().values()) {
                        Calendar cal2 = Calendar.getInstance();
                        cal2.add(Calendar.MONTH, -1);
                        if (unaVen.getFechaVenta().after(cal2)) {
                            for (Object iter : unaVen.getDetallesVenta().values()) {
                                DetalleVenta unDet = (DetalleVenta) iter;
                                int ventasMes = unaEmpresa.getArticulos().get(unDet.getUnArticulo().getCodBarra()).getVentasMes();
                                ventasMes += unDet.getCantidad();
                                unaEmpresa.getArticulos().get(unDet.getUnArticulo().getCodBarra()).setVentasMes(ventasMes);
                            }
                        }
                    }
                    Comparator<Articulo> comparador = new Comparator<Articulo>() {
                         
                        public int compare( Articulo a,Articulo b ) {
                            int resultado = Integer.compare( b.getVentasMes(), a.getVentasMes());
                            if ( resultado != 0 ) { return resultado;}
                            return resultado;
                        }
                    };
                    List lista = new ArrayList(unaEmpresa.getArticulos().values());
                    Collections.sort(lista,comparador);
                    //Iterator
                    int i = 0;
                    while (i < lista.size() && i <= 99){
                        JOptionPane.showMessageDialog(null, "en el while");
                        Articulo unArt = (Articulo)lista.get(i);
                        unArt.setEnVidriera(true);
                        i++;
                    }
                    
                    for (Articulo unar : unaEmpresa.getArticulos().values()){
                        
                        for(Object iter : lista){
                            Articulo aux = (Articulo)iter;
                            if (aux.getCodBarra() == unar.getCodBarra() && aux.isEnVidriera()){
                                unar.setEnVidriera(true);
                            }
                        }
                    }
                }
                JOptionPane.showMessageDialog(null, "Caja abierta.");
            }

            this.dispose();
        } catch (Exception ex) {
        }
    }
