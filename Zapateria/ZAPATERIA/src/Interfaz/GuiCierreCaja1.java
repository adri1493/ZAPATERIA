    private void txtSaldoEsperadoKeyTyped(java.awt.event.KeyEvent evt) { //GEN-FIRST:event_txtSaldoEsperadoKeyTyped
        char c = evt.getKeyChar();
        if (((c < '0') || (c > '9')) && (c != KeyEvent.VK_BACK_SPACE) && (c != '.')) {
            evt.consume();
        }
        if (c == '.' && txtSaldoEsperado.getText().contains(".")) {
            evt.consume();
        }
        if (txtSaldoEsperado.getText().length() >= 13) {
            evt.consume();
        }
    } //GEN-LAST:event_txtSaldoEsperadoKeyTyped

