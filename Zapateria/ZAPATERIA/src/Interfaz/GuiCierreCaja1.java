    public void cargar() {
        Double saldoSistema = cajaAbierta().getSaldoInicial();
        double otros = 0.0;
        DefaultListModel modelo = new DefaultListModel();
        for (int i = 2; i <= unaEmpresa.getTiposPagos().size(); i++) {
            for (MovimientoCaja unMov : cajaAbierta().getMovimientosCaja().values()) {
                for (Object unPago : unMov.getUnaVenta().getPagosVentas().values()) {
                    Pago unP = (Pago) unPago;
                    if (unP.getUnTipoPago() == unaEmpresa.getTiposPagos().get(1)) {
                        saldoSistema += unP.getMonto();
                    } else if (unP.getUnTipoPago() == unaEmpresa.getTiposPagos().get(i)) {
                        otros += unP.getMonto();
                    }
                }

            }
            modelo.addElement(unaEmpresa.getTiposPagos().get(i) + " = " + otros);
        }
        lblSaldoSistema.setText(String.valueOf(saldoSistema));
        lstFormaPago.setModel(modelo);
    }

    public Caja cajaAbierta() {
        Caja unaCa = null;
        for (Caja unaCaja : unaEmpresa.getCajas().values()) {
            if (unaCaja.isCajaAbierta()) {
                unaCa = unaCaja;
            }
        }
        return unaCa;
    }
