    private void menuModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuModificarActionPerformed
        txtDocumento.setEnabled(false);
        int i = tblUsuario.getSelectedRow();
        if (i == -1) {
            JOptionPane.showMessageDialog(null, "seleccione una fila");
        } else {
            Empleado aux;
            String nroDoc = tblUsuario.getValueAt(i, 0).toString();
            aux = unaEmpresa.getEmpleados().get(nroDoc);
            txtDocumento.setText(nroDoc);
            txtTelefono.setText(aux.getTelEmpleado());
            txtApellido.setText(aux.getApellido());
            txtDomicilio.setText(aux.getDomEmpleado());
            txtNombre.setText(aux.getNombreEmpleado());
            if (aux.isEstado())
                ckbActivar.setSelected(true);
            if (!aux.getPasword().isEmpty()) {
                ckbEsUsuario.setSelected(true);
                txtContrasena.setText(aux.getPasword());
                txtUser.setText(aux.getUsser());
                cmbRol.setSelectedItem(aux.getUnRol());
            }
        }
    }//GEN-LAST:event_menuModificarActionPerformed

