    public void CargaTabla(Map empleados) {
        DefaultTableModel modelo = (DefaultTableModel) tblUsuario.getModel();
        Collection lista;
        int filas = tblUsuario.getRowCount();
        for (int i = 0; filas > i; i++)
            modelo.removeRow(0);
        lista = empleados.values();
        if (!lista.isEmpty()) {
            int i = 0;
            for (Object unObj : lista) {
                Boolean esUsuario = false;
                Empleado unUs = (Empleado) unObj;
                if (unUs.getPasword() != null)
                    esUsuario = true;
                modelo.addRow(new Object[] { (unUs.getNroDocEmppleado()), (unUs.getNombreEmpleado()), unUs.getApellido(), esUsuario });
            }
        }
    }

