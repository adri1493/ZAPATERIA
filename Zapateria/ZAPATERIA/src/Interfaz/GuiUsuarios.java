    private void btnGuardarCerProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCerProActionPerformed
        if (!VerificarUsu(txtUser.getText())) {
            Boolean estado = false;
            Calendar fechaActual = new GregorianCalendar().getInstance();
            Boolean hayError = false;
            try {

                if ((!txtDocumento.getText().equals("")) && (!txtNombre.getText().equals("")) && (!txtApellido.getText().equals("")) &&
                    (!txtPorcentaje.getText().equals(""))) {
                    try {
                        lblApellido.setForeground(Color.black);
                        lblDomicilio.setForeground(Color.black);
                        lblNombre.setForeground(Color.black);
                        lblTelefono.setForeground(Color.black);
                        lblDocumento.setForeground(Color.black);
                        if (txtDocumento.isEnabled()) {
                            if (ckbEsUsuario.isSelected()) {
                                unaEmpresa.agregarEmpleadoUsuario(txtDocumento.getText(), txtNombre.getText(), txtDomicilio.getText(), txtTelefono.getText(),
                                                                  txtApellido.getText(), txtContrasena.getText(), fechaActual, estado, (Rol) cmbRol.getSelectedItem(),
                                                                  txtUser.getText(), Integer.parseInt(txtPorcentaje.getText()));
                                limpiar();
                                CargaTabla(unaEmpresa.getEmpleados());
                                JOptionPane.showMessageDialog(null, "El Usuario se cargo con exito");

                            } else {
                                unaEmpresa.agregarEmpleado(txtDocumento.getText(), txtNombre.getText(), txtDomicilio.getText(), txtTelefono.getText(), txtApellido.getText(),
                                                           fechaActual, estado, Integer.parseInt(txtPorcentaje.getText()));
                                JOptionPane.showMessageDialog(null, "El Empleado se cargo con exito");
                            }
                        } else {
                            if (ckbEsUsuario.isSelected()) {
                                unaEmpresa.modificarEmpleadoUsuario(txtDocumento.getText(), txtNombre.getText(), txtDomicilio.getText(), txtTelefono.getText(),
                                                                    txtApellido.getText(), txtContrasena.getText(), fechaActual, estado, (Rol) cmbRol.getSelectedItem(),
                                                                    txtUser.getText());
                                JOptionPane.showMessageDialog(null, "El Usuario se modifico con exito");
                            } else {
                                unaEmpresa.modificarEmpleado(txtDocumento.getText(), txtNombre.getText(), txtDomicilio.getText(), txtTelefono.getText(), txtApellido.getText(),
                                                             fechaActual, estado);
                                JOptionPane.showMessageDialog(null, "El Empleado se modifico con exito");
                            }
                        }
                        limpiar();
                        CargaTabla(unaEmpresa.getEmpleados());

                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    }
                } else {
                    if (txtApellido.getText().equals("")) {
                        lblApellido.setForeground(Color.red);
                        hayError = true;
                    } else {
                        lblApellido.setForeground(Color.black);
                    }
                    if (txtDomicilio.getText().equals("")) {
                        lblDomicilio.setForeground(Color.red);
                        hayError = true;
                    } else {
                        lblDomicilio.setForeground(Color.black);
                    }
                    if (txtNombre.getText().equals("")) {
                        lblNombre.setForeground(Color.red);
                        hayError = true;
                    } else {
                        lblNombre.setForeground(Color.black);
                    }
                    if (txtDocumento.getText().equals("")) {
                        lblDocumento.setForeground(Color.red);
                        hayError = true;
                    } else {
                        txtDocumento.setForeground(Color.black);
                    }
                    if (txtTelefono.getText().equals("")) {
                        lblTelefono.setForeground(Color.red);
                        hayError = true;
                    } else {
                        lblTelefono.setForeground(Color.black);
                    }
                    /*if (txtTelefono.getText().equals("") && (txtDomicilio.getText().equals(""))) {
                            JOptionPane.showMessageDialog(null, "se cargo con exito6");
                        }*/

                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        } else
            JOptionPane.showMessageDialog(null, "el nombre de usuario ya se encuentra en uso");

    }//GEN-LAST:event_btnGuardarCerProActionPerformed

