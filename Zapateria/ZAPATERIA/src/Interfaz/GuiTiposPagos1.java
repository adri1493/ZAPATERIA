    public void cargar(Map tiposPagos) {
        Collection lista;
        lista = tiposPagos.values();
        DefaultListModel modelo = new DefaultListModel();
        if (!lista.isEmpty()) {
            int i = 0;
            for (Object unObj : lista) {
                TipoPago unTipoPago = (TipoPago) unObj;
                modelo.addElement(unTipoPago.getNombre());

            }
            lstTipoPago.setModel(modelo);
        }
    }

    public void limpiar() {
        cbkCbu.setSelected(false);
        cbkNombreBanco.setSelected(false);
        cbkNumeroCuenta.setSelected(false);
        cbkNumeroDocumento.setSelected(false);
        cbkTipoCuenta.setSelected(false);
        cbkTitular.setSelected(false);
    }

    public Boolean[] permiso() {
        Boolean[] permisos = new Boolean[6];
        if (!txtNombreTipoPago.getText().equals("")) {
            if (cbkCbu.isSelected()) {
                permisos[0] = true;
            } else {
                permisos[0] = false;
            }
            if (cbkNombreBanco.isSelected()) {
                permisos[1] = true;
            } else {
                permisos[1] = false;
            }
            if (cbkNumeroCuenta.isSelected()) {
                permisos[2] = true;
            } else {
                permisos[2] = false;
            }
            if (cbkNumeroDocumento.isSelected()) {
                permisos[3] = true;
            } else {
                permisos[3] = false;
            }
            if (cbkTipoCuenta.isSelected()) {
                permisos[4] = true;
            } else {
                permisos[4] = false;
            }
            if (cbkTitular.isSelected()) {
                permisos[5] = true;
            } else {
                permisos[5] = false;
            }
        } else
            JOptionPane.showMessageDialog(null, "no ingreso el nombre");
        return permisos;
    }

    private Boolean aprobado() {
        Boolean existe = true;
        if (!(txtNombreTipoPago.getText().equals(""))) {
            for (TipoPago tipo : unaEmpresa.getTiposPagos().values()) {
                if (tipo.getNombre().equals(txtNombreTipoPago.getText()))
                    existe = false;
            }
        } else
            existe = false;
        return existe;
    }

    private void reloj() {
        javax.swing.Timer timer = new javax.swing.Timer(1000, new java.awt.event.ActionListener() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent ae) {
                lblFecha.setText(Calendar.getInstance().getTime().toLocaleString());
            }
        });
        timer.start();
    }
