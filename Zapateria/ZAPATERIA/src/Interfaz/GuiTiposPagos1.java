    private void menuModificarActionPerformed(java.awt.event.ActionEvent evt) { //GEN-FIRST:event_menuModificarActionPerformed
        int i = lstTipoPago.getSelectedIndex();
        if (i == -1) {
            JOptionPane.showMessageDialog(null, "no selecciono un Tipo Pago");
        } else {
            lstTipoPago.setEnabled(false);
            txtNombreTipoPago.setText(lstTipoPago.getSelectedValue().toString());
            limpiar();
            TipoPago unTipo = unaEmpresa.getTiposPagos().get(txtNombreTipoPago.getText());
            Boolean[] permiso = unTipo.getParametros();
            if (permiso[0])
                cbkCbu.setSelected(true);
            if (permiso[1])
                cbkNombreBanco.setSelected(true);
            if (permiso[2])
                cbkNumeroCuenta.setSelected(true);
            if (permiso[3])
                cbkNumeroDocumento.setSelected(true);
            if (permiso[4])
                cbkTipoCuenta.setSelected(true);
            if (permiso[5])
                cbkTitular.setSelected(true);
        }
    } //GEN-LAST:event_menuModificarActionPerformed

