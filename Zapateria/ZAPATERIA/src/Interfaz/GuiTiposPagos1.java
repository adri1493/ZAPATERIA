    private void btnGuardarNueTipoPagoActionPerformed(java.awt.event.ActionEvent evt) { //GEN-FIRST:event_btnGuardarNueTipoPagoActionPerformed
        if (aprobado()) {
            if (!lstTipoPago.isEnabled()) {
                try {
                    unaEmpresa.modificarTipoPago(lstTipoPago.getSelectedValue().toString(), txtNombreTipoPago.getText(), permiso());
                    JOptionPane.showMessageDialog(null, "El tipo de pago se modifico con exito");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
                lstTipoPago.setEnabled(true);

            } else {
                try {
                    unaEmpresa.agregarTipoPago(txtNombreTipoPago.getText(), permiso());
                    JOptionPane.showMessageDialog(null, "El tipo de pago se cargo con exito");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }
            cargar(unaEmpresa.getTiposPagos());
            limpiar();
            txtNombreTipoPago.setText("");
        } else
            JOptionPane.showMessageDialog(null, "No ingreso el Nombre o el nombre ya existe", null, JOptionPane.INFORMATION_MESSAGE);

    } //GEN-LAST:event_btnGuardarNueTipoPagoActionPerformed

