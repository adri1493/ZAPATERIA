    private void menuModificarActionPerformed(java.awt.event.ActionEvent evt) { //GEN-FIRST:event_menuModificarActionPerformed
        nroFila = tblTipoPago.getSelectedRow();
        Double j = 0.0;
        Double h = 0.0;
        String elemento;
        if (nroFila == -1) {
            JOptionPane.showMessageDialog(null, "seleccione una fila");
        } else {
            DefaultTableModel modelo = (DefaultTableModel) tblTipoPago.getModel();
            elemento = String.valueOf(modelo.getValueAt(nroFila, 0));
            modelo.removeRow(nroFila);
            int filas = tblTipoPago.getRowCount();
            for (int i = 0; filas > i; i++) {
                j = j + Double.parseDouble(modelo.getValueAt(i, 1).toString());
                h = h + Double.parseDouble(modelo.getValueAt(i, 2).toString());
            }
            lblTotalesMonto.setText(String.valueOf(j));
            lblTotalesPorc.setText(String.valueOf(h));

            for (Iterator iter = pagos.iterator(); iter.hasNext();) {
                PagoCompra unP = (PagoCompra) iter;
                if (unP.getUnTipoPago().getNombre().equals(elemento)) {
                    txtCBU.setText(unP.getCBU());
                    cmbFormaPago1.addItem(unP.getUnTipoPago());
                    cmbFormaPago1.setSelectedItem(unP.getUnTipoPago());
                    txtNombreBanco.setText(unP.getNombreBanco());
                    txtTitular.setText(unP.getTitular());
                    txtNumeroDoc.setText(unP.getNumeroDocumento());
                    txtNroCuenta.setText(unP.getNumeroCuenta());
                    txtFP.setText(String.valueOf(unP.getMonto()));
                    pagos.remove(iter);
                }
            }

        }
        Component[] components = jPanel5.getComponents();
        for (int i = 0; i < components.length; i++) {
            components[i].setEnabled(true);
            if (components[i] instanceof Container) {
                components[i].setEnabled(true);
            }
        }
    } //GEN-LAST:event_menuModificarActionPerformed

