    private void btnGuardarFormaPagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarFormaPagoActionPerformed//GEN-LAST:event_menuEliminarActionPerformed
        if (lblTotalesPorc.getText().equals("100.0")) {//GEN-FIRST:event_btnGuardarFormaPagoActionPerformed
            DefaultTableModel modeloTipo = (DefaultTableModel) tblTipoPago.getModel();
            int filasTipo = modeloTipo.getRowCount();
            try {
                Articulo aux;
                if (esCompra) {
                    int compra = unaEmpresa.agregarCompra(unPro, unUsuario, Total, fechaCompra);
                    Compra unaCompra = (Compra) (unaEmpresa.getCompras().get(compra));
                    int filas = modeloCompra.getRowCount();
                    for (int i = 0; filas > i; i++) {
                        unaCompra.agregarDetalleCompra(Integer.parseInt(modeloCompra.getValueAt(i, 4).toString()),
                                                       Double.parseDouble(modeloCompra.getValueAt(i, 5).toString()),
                                                       (Articulo) unaEmpresa.getArticulos().get(modeloCompra.getValueAt(i, 0)));
                        aux = (Articulo) unaEmpresa.getArticulos().get(modeloCompra.getValueAt(i, 0));
                        aux.setStock(aux.getStock() + Integer.parseInt(modeloCompra.getValueAt(i, 4).toString()));

                    }

                    unaCompra.setPagosCompras(pagos);
                    JOptionPane.showMessageDialog(null, "La compra se cargo con exito");
                    this.dispose();
                } else {
                    int venta = unaEmpresa.agregarVenta(unEmpleado, unUsuario, Total, fechaVenta, nombreCliente, domCliente, nroDocCliente);
                    Venta unaVenta = (Venta) (unaEmpresa.getVentas().get(venta));
                    int filas = modeloVenta.getRowCount();
                    for (int i = 0; filas > i; i++) {
                        unaVenta.agregarDetalleVenta(Integer.parseInt(modeloVenta.getValueAt(i, 4).toString()), Double.parseDouble(modeloVenta.getValueAt(i, 5).toString()),
                                                     (Articulo) unaEmpresa.getArticulos().get(modeloVenta.getValueAt(i, 0)));
                        aux = (Articulo) unaEmpresa.getArticulos().get(modeloVenta.getValueAt(i, 0));
                        aux.setStock(aux.getStock() - Integer.parseInt(modeloVenta.getValueAt(i, 4).toString()));
                    }
                    unaVenta.setPagosVentas(pagos);
                    Caja unaCaja = codCaja();
                    unaCaja.agregarMovimientoCaja(unaVenta);
                    JOptionPane.showMessageDialog(null, "La venta se cargo con exito");
                    this.dispose();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        } else
            JOptionPane.showMessageDialog(null, "todabia queda saldo");
    }//GEN-LAST:event_btnGuardarFormaPagoActionPerformed

