    public void actualizar(DefaultTableModel modelo) {
        int filas = tblTipoPago.getRowCount();
        Boolean entro = false;
        DefaultComboBoxModel modeloCmb = new DefaultComboBoxModel();
        for (TipoPago tipo : unaEmpresa.getTiposPagos().values()) {
            entro = false;
            for (int i = 0; filas > i; i++) {
                if (tipo.getNombre().equals(String.valueOf(modelo.getValueAt(i, 0)))) {
                    entro = true;
                }
            }
            if (entro == false) {
                modeloCmb.addElement(tipo);
            }
        }
        cmbFormaPago1.setModel(modeloCmb);
    }
